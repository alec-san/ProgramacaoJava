    private void limparCampos() {
        txfId.setText(" ");
        txfNomeAutor.setText(" ");
        txfNomeAutor.requestFocus();
        txfPseudoNomeAutor.setText(" ");

        btnCadastrar.setEnabled(true);
        btnAtualizar.setEnabled(false);

    }

    private void listarAutor() {
        try {
            AutorDAO objAutorDao = new AutorDAO();
            DefaultTableModel model = (DefaultTableModel) tblAutor.getModel();
            model.setNumRows(0);

            ArrayList<AutorDTO> lista = objAutorDao.PesquisarAutor();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId(),
                    lista.get(num).getNome(),
                    lista.get(num).getPseudonimo()
                });
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Valores VIEW" + erro);
        }

    }

