    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String nomeAutor = txfNomeAutor.getText();
        String pseudoNomeAutor = txfPseudoNomeAutor.getText();

        AutorDTO objAutorDto = new AutorDTO();
        objAutorDto.setNome(nomeAutor);
        objAutorDto.setPseudonimo(pseudoNomeAutor);

        AutorDAO objAutorDao = new AutorDAO();
        objAutorDao.CadastrarAutor(objAutorDto);

        limparCampos();
        listarAutor();

    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void tblAutorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAutorMouseClicked
        // TODO add your handling code here:
        //set data to their textfield

        DefaultTableModel tabela = (DefaultTableModel) tblAutor.getModel();

        //set data to text field when raw is selected
        String tblId = tabela.getValueAt(tblAutor.getSelectedRow(), 0).toString();
        String tblNome = tabela.getValueAt(tblAutor.getSelectedRow(), 1).toString();
        String tblPseudo = tabela.getValueAt(tblAutor.getSelectedRow(), 2).toString();

        //set to textField
        //txfId.setEnabled(true);
        txfId.setText(tblId);
        txfNomeAutor.setText(tblNome);
        txfPseudoNomeAutor.setText(tblPseudo);

        btnCadastrar.setEnabled(false);
        btnAtualizar.setEnabled(true);
    }//GEN-LAST:event_tblAutorMouseClicked

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        
        Long idAutor = Long.parseLong(txfId.getText());
        String nomeAutor = txfNomeAutor.getText();
        String pseudoNomeAutor = txfPseudoNomeAutor.getText();

        AutorDTO objAutorDto = new AutorDTO();
        objAutorDto.setId(idAutor);
        objAutorDto.setNome(nomeAutor);
        objAutorDto.setPseudonimo(pseudoNomeAutor);

        AutorDAO objAutorDao = new AutorDAO();
        objAutorDao.AtualizarAutor(objAutorDto);

        limparCampos();
        listarAutor();

    }//GEN-LAST:event_btnAtualizarActionPerformed

