    private void btnConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConverterActionPerformed
        // TODO add your handling code here:

        if (rdbBinario.isSelected() == true) {
            Binario bin = new Binario();
            int numero = Integer.parseInt(txfEntrada.getText());
            String retorno = bin.ConverteBinario(numero);
            txfEntrada.setText(retorno);
        }else{
            Decimal deci = new Decimal();
            int numero = Integer.parseInt(txfEntrada.getText());
            String retorno = String.valueOf(deci.converteDecimal(numero));
            txfEntrada.setText(retorno);
        }

    }//GEN-LAST:event_btnConverterActionPerformed

